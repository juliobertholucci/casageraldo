        <h5 class="fontClass SubTituloPasseio">Como Funciona?</h5>
        
        <p> 
        
                O "Passeio Prata" serve como porta de entrada para o mundo vitivinícola. Por cerca de uma hora e meia, os visitantes conhecem aspectos mais básicos deste universo.
                <br><br>
                A visitação inicia-se com o check-in no átrio da Casa Geraldo, onde o visitante adquire uma pulseira que deve ser mantida durante todo o trajeto. Recomenda-se que os participantes cheguem cerca de vinte minutos antes do horário marcado.
                 <br><br>
                Na sequencia todos se dirigem ao auditório, onde são recebidos por um guia capacitado que lhes presenteia com taças personalizadas e apresenta um vídeo contando a história da vinicola.  
                <br><br>
                Do auditório o grupo caminha cerca de 100 metros pelo parreiral de uva Shiraz ouvindo explicações sobre o método “dupla poda” de cultivo de uvas que resulta nos vinhos finos “colheita de inverno”; na área de vinificação são apresentadas as formas como vinhos e espumantes são elaborados. 
                <br><br>
                Tem até mesmo aquele momento único onde os enófilos passam por um túnel que demonstra uma das formas usadas pelos vitivinicultores para a guarda dos vinhos.
                <br><br>
                O Passeio Prata é marcado pela degustação de oito vinhos, entre secos e suaves. São servidos espumantes, vinhos Reserva e um vinho fortificado. Tudo isso harmonizado com queijos de Minas e com o azeite Extra Virgem da Casa Geraldo.
                <br><br>
                Sucos branco e tintos também são oferecidos. Ao término, o visitante pode degustar ainda outros rótulos e decidir qual levar para casa na loja da Casa Geraldo. Quem quiser ampliar ainda mais esta incrível experiência sensorial pode deixar reservada uma mesa em nosso restaurante, um dos mais renomados pontos enogastronômicos do país.
        </p>
        
